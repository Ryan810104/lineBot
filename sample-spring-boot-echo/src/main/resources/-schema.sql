

CREATE TABLE user (
                          number INT AUTO_INCREMENT  PRIMARY KEY,
                          time VARCHAR(250) NOT NULL,
                          jpy VARCHAR(250) NOT NULL,
                          usd VARCHAR(250) NOT NULL,
                          eur VARCHAR(250) NOT NULL,
                          vnd VARCHAR(250) NOT NULL,
                          cny VARCHAR(250) NOT NULL,
                          hkd VARCHAR(250) NOT NULL,
                          sgd VARCHAR(250) NOT NULL,
                          gbp VARCHAR(250) NOT NULL,
                          thb VARCHAR(250) NOT NULL
);
